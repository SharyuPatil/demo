package IfElseDemo;

public class ForExample
{
    public static void main(String[] args)
    {
        int [] a={20,30,40,50};
        for(int j:a)
        {
            System.out.print(j);
            System.out.print(",");

        }
        System.out.println("\n");
        String [] names={"s","a","m","r"};
        for (String name:names)
        {
            System.out.print(name);
            System.out.print(",");
        }


    }
}
